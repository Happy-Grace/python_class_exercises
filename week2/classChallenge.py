# TEMPERATURE CONVERSION FROM CELSIUS (°C) TO FAHRENHEIT (°F)

# Prompts to request the temperature value to be converted from User.

temp = input("Enter temperature in celsius: \t")
celsius = float(temp)

fahrenheit = (celsius * 9 / 5) + 32

print(fahrenheit, '°F')



